package q3Project.main;

public class Notes {
    private String type;
    private String text;
    
    public void setType(String type) {
        this.type = type;
    }
    public void setText(String text) {
        this.text = text;
    }
    public String getNoteType() {
        return type;
    }
    public String getNoteText() {
        return text;
    }
}
